# Teoria: operadores logicos

# Mayor estricto >
print("Mayor estricto 3 > 2", 3 > 2)

# Menor estricto <
print("Menor estricto 2 < 2", 2 < 2)

# Mayor o igual >=
print("Mayor o igual 2 >= 2 ", 2 >= 2)

# Menor o igual <= 
print("Menor o igual 2 <= 1 ", 2 <= 1)

# Es igual == 
print("Es igual 2 == 1 ", 2 == 1)

# Diferente
print("Diferente 2 != 2 ", 2 != 2)

print("Operacion:", 1 < 2 and 2 != 2)

# Rango
print("Rango 1 < 2 != 3 ", 1 < 2 != 3)